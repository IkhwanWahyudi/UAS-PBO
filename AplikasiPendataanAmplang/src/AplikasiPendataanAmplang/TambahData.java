    private void updateStok(int Stok, int StokLama, int ID) {
        String query = "UPDATE tbAmplang SET `Stok`=? WHERE `ID` = ?";
        try (PreparedStatement statement = db.conn.prepareStatement(query)) {
            // Mengatur nilai parameter untuk data yang akan diubah
            int StokBaru = Stok + StokLama;
            statement.setInt(1, StokBaru);
            // Mengatur nilai parameter untuk kondisi WHERE
            statement.setInt(2, ID);

            // Eksekusi query UPDATE
            int rowsUpdated = statement.executeUpdate();

            if (rowsUpdated > 0) {
                // Data berhasil diubah
                JOptionPane.showMessageDialog(this,
                        "Data yang dimasukkan sudah ada, maka stok ditambahkan!",
                        "Sukses", JOptionPane.INFORMATION_MESSAGE);
            } else {
                // Tidak ada data yang terpenuhi kondisi WHERE, atau tidak ada perubahan data
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Error saat menjalankan query: " + e.getMessage(),
                    "Gagal", JOptionPane.ERROR_MESSAGE);
        }

    }

    private void jButtonTambahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonTambahMouseClicked
        String rasa = (String) jComboBox1.getSelectedItem();
        String ukuran = "";
        String stokStr = "";
        String umur = (String) jComboBoxUmur.getSelectedItem();
        String hargaStr = "";

        try {
            hargaStr = jTextFieldHarga.getText();
            int harga = Integer.parseInt(hargaStr);

            try {
                stokStr = jTextFieldStok.getText();
                int stok = Integer.parseInt(stokStr);

                boolean isValidSelection = false;
                if (jRadioButton1.isSelected()) {
                    ukuran = "80 Gram";
                    isValidSelection = true;
                } else if (jRadioButton2.isSelected()) {
                    ukuran = "100 Gram";
                    isValidSelection = true;
                } else if (jRadioButton3.isSelected()) {
                    ukuran = "150 Gram";
                    isValidSelection = true;
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Ukuran tidak valid, pilih ukuran!",
                            "Gagal", JOptionPane.ERROR_MESSAGE);
                }

                if (isValidSelection) {
                    String query = "SELECT * FROM tbAmplang WHERE `Rasa` = ? AND `Ukuran` = ? AND `Umur Simpan` = ? AND `Harga` = ?";
                    try (PreparedStatement statement = db.conn.prepareStatement(query)) {
                        // Mengatur nilai parameter
                        statement.setString(1, rasa);
                        statement.setString(2, ukuran);
                        statement.setString(3, umur);
                        statement.setInt(4, harga);

                        // Eksekusi query SELECT
                        ResultSet resultSet = statement.executeQuery();

                        // Memeriksa hasil query
                        if (resultSet.next()) {
                            // Data sudah ada di database
                            String IDStr = resultSet.getString("ID");
                            int ID = Integer.parseInt(IDStr);

                            String StokStr = resultSet.getString("Stok");
                            int Stok = Integer.parseInt(StokStr);

                            updateStok(stok, Stok, ID);
                            showData();
                            kosong();
                        } else {
                            // Data belum ada di database
                            // Lakukan insert data
                            insertData(rasa, ukuran, stok, umur, harga);
                            showData();
                            kosong();
                        }
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(this,
                                "Error saat menjalankan query: " + e.getMessage(),
                                "Gagal", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } catch (NumberFormatException e) {
                // Penanganan ketika teks tidak dapat diubah menjadi integer
                JOptionPane.showMessageDialog(this,
                        "Stok tidak valid, masukkan angka!",
                        "Gagal", JOptionPane.ERROR_MESSAGE);
                jTextFieldStok.setText("");
            }
        } catch (NumberFormatException e) {
            // Penanganan ketika teks tidak dapat diubah menjadi integer
            JOptionPane.showMessageDialog(this,
                    "Harga tidak valid, masukkan angka!",
                    "Gagal", JOptionPane.ERROR_MESSAGE);
            jTextFieldHarga.setText("");
        }
    }//GEN-LAST:event_jButtonTambahMouseClicked

