    private void showData() {
        try {
            String query = "SELECT `Rasa`, `Ukuran`, `Stok`, `Umur Simpan`, `Harga` FROM `tbamplang`";
            db.st = db.conn.createStatement();
            db.rs = db.st.executeQuery(query);

            // Dapatkan metadata kolom
            ResultSetMetaData metaData = db.rs.getMetaData();
            int columnCount = metaData.getColumnCount();

            // Buat model tabel
            DefaultTableModel model = new DefaultTableModel();

            // Tambahkan nama kolom ke model
            for (int columnIndex = 1; columnIndex <= columnCount; columnIndex++) {
                model.addColumn(metaData.getColumnName(columnIndex));
            }

            // Tambahkan data ke model
            while (db.rs.next()) {
                Object[] row = new Object[columnCount];
                for (int columnIndex = 1; columnIndex <= columnCount; columnIndex++) {
                    row[columnIndex - 1] = db.rs.getObject(columnIndex);
                }
                model.addRow(row);
            }

            // Set model pada JTable
            jTable1.setModel(model);

            db.rs.close();
            db.st.close();
        } catch (SQLException e) {
            System.out.println("Error saat mengambil data: " + e.getMessage());
        }
    }

    private void insertData(String rasa, String ukuran, int stok, String umur, int harga) {
        try {
            String query = "INSERT INTO `tbamplang`(`Rasa`, `Ukuran`, `Stok`, `Umur Simpan`, `Harga`) VALUES (?, ?, ?, ?, ?)";
            // Mengatur nilai parameter
            try (PreparedStatement statement = db.conn.prepareStatement(query)) {
                // Mengatur nilai parameter
                statement.setString(1, rasa);
                statement.setString(2, ukuran);
                statement.setInt(3, stok);
                statement.setString(4, umur);
                statement.setInt(5, harga);

                // Eksekusi pernyataan INSERT
                int rowsInserted = statement.executeUpdate();

                if (rowsInserted > 0) {
                    JOptionPane.showMessageDialog(this,
                            "Data berhasil ditambahkan!",
                            "Sukses", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Gagal menambahkan data!",
                            "Gagal", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException e) {
            System.out.println("Error saat memasukkan data: " + e.getMessage());
        }
    }

