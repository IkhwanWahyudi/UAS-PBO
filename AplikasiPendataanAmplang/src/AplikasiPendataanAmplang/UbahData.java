    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int selectedRow = jTable1.getSelectedRow();

        Object value = jTable1.getValueAt(selectedRow, 0);
        String selectedValue = value.toString();
        jTextFieldID.setText(selectedValue);

        Object value1 = jTable1.getValueAt(selectedRow, 1);
        String selectedValue1 = value1.toString();
        jComboBox1.setSelectedItem(selectedValue1);

        Object value2 = jTable1.getValueAt(selectedRow, 2);
        if (value2.equals("80 Gram")) {
            jRadioButton1.setSelected(true);
            jRadioButton2.setSelected(false);
            jRadioButton3.setSelected(false);
        } else if (value2.equals("100 Gram")) {
            jRadioButton1.setSelected(false);
            jRadioButton2.setSelected(true);
            jRadioButton3.setSelected(false);
        } else if (value2.equals("150 Gram")) {
            jRadioButton1.setSelected(false);
            jRadioButton2.setSelected(false);
            jRadioButton3.setSelected(true);
        } else {
            // Penanganan jika nilai tidak sesuai
            jRadioButton1.setSelected(false);
            jRadioButton2.setSelected(false);
            jRadioButton3.setSelected(false);
        }

        Object value3 = jTable1.getValueAt(selectedRow, 3);
        String selectedValue3 = value3.toString();
        jTextFieldStok.setText(selectedValue3);

        Object value4 = jTable1.getValueAt(selectedRow, 4);
        String selectedValue4 = value4.toString();
        jComboBoxUmur.setSelectedItem(selectedValue4);

        Object value5 = jTable1.getValueAt(selectedRow, 5);
        String selectedValue5 = value5.toString();
        jTextFieldHarga.setText(selectedValue5);
    }//GEN-LAST:event_jTable1MouseClicked

