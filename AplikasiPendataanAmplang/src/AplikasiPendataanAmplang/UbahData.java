    private void updateData(String rasa, String ukuran, int stok, String umur, int harga, int ID) {
        try {
            String query = "UPDATE `tbamplang` SET `Rasa`=?,`Ukuran`=?,`Stok`=?,`Umur Simpan`=?,`Harga`=? WHERE `ID`=?";
            // Mengatur nilai parameter
            try (PreparedStatement statement = db.conn.prepareStatement(query)) {
                // Mengatur nilai parameter
                statement.setString(1, rasa);
                statement.setString(2, ukuran);
                statement.setInt(3, stok);
                statement.setString(4, umur);
                statement.setInt(5, harga);
                statement.setInt(6, ID);

                // Eksekusi pernyataan UPDATE
                int rowsInserted = statement.executeUpdate();

                if (rowsInserted > 0) {
                    JOptionPane.showMessageDialog(this,
                            "Data berhasil diubah!",
                            "Sukses", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Gagal mengubah data!",
                            "Gagal", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException e) {
            System.out.println("Error saat memasukkan data: " + e.getMessage());
        }
    }

    private void jButtonUbahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonUbahMouseClicked

        String rasa = (String) jComboBox1.getSelectedItem();
        String ukuran = "";
        String stokStr = "";
        String umur = (String) jComboBoxUmur.getSelectedItem();
        String hargaStr = "";
        String IDStr = "";

        try {
            IDStr = jTextFieldID.getText();
            int ID = Integer.parseInt(IDStr);

            try {
                stokStr = jTextFieldStok.getText();
                int stok = Integer.parseInt(stokStr);

                try {
                    hargaStr = jTextFieldHarga.getText();
                    int harga = Integer.parseInt(hargaStr);

                    boolean isValidSelection = false;
                    if (jRadioButton1.isSelected()) {
                        ukuran = "80 Gram";
                        isValidSelection = true;
                    } else if (jRadioButton2.isSelected()) {
                        ukuran = "100 Gram";
                        isValidSelection = true;
                    } else if (jRadioButton3.isSelected()) {
                        ukuran = "150 Gram";
                        isValidSelection = true;
                    } else {
                        JOptionPane.showMessageDialog(this,
                                "Ukuran tidak valid, pilih ukuran!",
                                "Gagal", JOptionPane.ERROR_MESSAGE);
                    }

                    if (isValidSelection) {
                        // Lakukan update data
                        updateData(rasa, ukuran, stok, umur, harga, ID);
                        showData();
                        kosong();
                    }
                } catch (NumberFormatException e) {
                    // Penanganan ketika teks tidak dapat diubah menjadi integer
                    JOptionPane.showMessageDialog(this,
                            "Harga tidak valid, masukkan angka!",
                            "Gagal", JOptionPane.ERROR_MESSAGE);
                    jTextFieldHarga.setText("");
                }
            } catch (NumberFormatException e) {
                // Penanganan ketika teks tidak dapat diubah menjadi integer
                JOptionPane.showMessageDialog(this,
                        "Stok tidak valid, masukkan angka!",
                        "Gagal", JOptionPane.ERROR_MESSAGE);
                jTextFieldStok.setText("");
            }
        } catch (NumberFormatException e) {
            // Penanganan ketika teks tidak dapat diubah menjadi integer
            JOptionPane.showMessageDialog(this,
                    "ID tidak valid, pilih data yang ada pada tabel!",
                    "Gagal", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonUbahMouseClicked

